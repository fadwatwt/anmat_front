export const getCityOptions = (selectedCountry) => {
    if (!selectedCountry) return [];
    const entry = countriesWithCities.find(c => c.country === selectedCountry);
    if (!entry) return [];
    return entry.cities.map(city => ({ _id: city, name: city }));
};
